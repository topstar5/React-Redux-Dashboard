import {
    INCREASE_TEAM_INDEX,
    REDUCE_TEAM_INDEX,
    CHANGE_TEAM_INDEX,
} from '../constants/actionTypes';

export function increaseTeamIndex () {
    return {
        type: INCREASE_TEAM_INDEX,

    }
}

export function reduceTeamIndex () {
    return {
        type: REDUCE_TEAM_INDEX,
    }
}

export function changeTeamIndex (teamIndex) {
    return {
        type: CHANGE_TEAM_INDEX,
        teamIndex
    }
}